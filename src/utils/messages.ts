import 'react-toastify/dist/ReactToastify.css';
import {toast} from 'react-toastify';


export function displayError(message: string) {
    toast.error(message, {
        position: "bottom-center",
        autoClose: 5000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "light",
        toastId: message,
      });
}
